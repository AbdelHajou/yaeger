package nl.meron.yaeger.engine.entities.entity;

/**
 * An {@link AnchorPoint} denotes the point of an {@link Entity} which is being used for
 * placement on the x,y-coordinate.
 */
public enum AnchorPoint {
    TOP_LEFT,
    TOP_CENTER,
    TOP_RIGHT,
    LEFT_CENTER,
    CENTER_CENTER,
    RIGHT_CENTER,
    BOTTOM_LEFT,
    BOTTOM_CENTER,
    BOTTOM_RIGHT;
}
